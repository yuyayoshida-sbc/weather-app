export function findFAQByKeyword(input: string): FAQItem | undefined {
  const lower = input.toLowerCase();
  return FAQ_DATA.find((faq) =>
    faq.keywords.some((keyword) => lower.includes(keyword.toLowerCase()))
  );
}
